bool loadFile( filePath ) char * filePath;
{
    bool ok;
    FILE * fp;
    char c, *buf, *pcolon, *pnext;
    uint16_t run_address, a;
    uint8_t b;
    char acLine[ 120 ];

    run_address = 0;
    ok = false;
    fp = fopen( filePath, "r" );

    if ( 0 != fp )
    {
        c = (char) fgetc( fp );
        fclose( fp ); /* can't seek on cp/m reliably */
        fp = fopen( filePath, "r" );

        if ( ':' == c )
            return load_intel( fp );

        a = 0;

        do
        {
            buf = fgets( acLine, _countof( acLine), fp );

            if ( buf && strlen( buf ) >= 3 )
            {
                pcolon = strchr( buf, ':' );
                if ( !pcolon )
                    usage( "input hex file has a line without a colon, which is invalid" );

                if ( pcolon != buf )
                {
                    a = (uint16_t) hextoui( buf );

                    if ( run_address < 0x100 )
                    {
                        run_address = a;
    
                        if ( !g_fStartAddressSpecified )
                            g_startAddress = a;
                    }
                }

                pnext = pcolon + 1;
                ok = true;

                for ( ;; )
                {
                    while ( ' ' == *pnext )
                        pnext++;

                    if ( ishex( *pnext ) && ishex( * ( pnext + 1 ) ) )
                    {
                        b = (uint8_t) hextoui( pnext );
                        pnext += 2;
                        set_byte( a++, b );
                    }
                    else
                        break;
                }
            }
        } while ( !feof( fp ) );

        fclose( fp );
    }

    return ok;
}

