uint8_t m_hook()
{
    uint16_t address;
    address = cpu.pc;

    if ( 0xff1f == address )
    {
        printf( "\n" );
        end_emulation();
    }
    else if ( 0xffdc == address )
        printf( "%02x", cpu.a );
    else if ( 0xffe5 == address )
        printf( "%x", 0xf & cpu.a );
    else if ( 0xffef == address )
    {
        if ( 0x0d != cpu.a )
            printf( "%c", toupper( cpu.a ) );
    }

    fflush( stdout );
    return OP_RTS;
}

