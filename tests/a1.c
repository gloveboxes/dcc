int bdos_kbhit()
{
    return bdos( 6, 0xff );
}

int getc_load_file()
{
    char c;

    c = fgetc( g_loadFile );
    if ( 26 == c )            /* ^z eof on CP/M */
        return EOF;
    if ( 3 == c || 17 == c )  /* ^c or ^q */
        end_emulation();

#ifdef HISOFTCPM
    if ( 0x0a == c )
        c = 0x0d;
#endif

    return c;
}

uint8_t m_load( address ) uint16_t address;
{
    char ch;
    int next;

    if ( 0xd011 == address )
    {
        if ( g_loadFile )
        {
            next = getc_load_file();
            if ( EOF == next )
            {
                fclose( g_loadFile );
                g_loadFile = 0;
            }
            else
            {
                kbd_char = (char) next;
                kbd_available = true;
                return 0x80;
            }
        }

        ch = (char) bdos_kbhit();
        if ( 0 != ch )
        {
            if ( 0xa == ch )
                ch = 0xd;

            if ( ch < 26 && 0x08 != ch && 0x0d != ch && 0x7f != ch )
            {
                if ( 17 == ch || 3 == ch )
                    end_emulation();
                else if ( 12 == ch )
                {
                    load_input_file();
                    if ( g_loadFile )
                        return 0x80;
                }
                else if ( 18 == ch )
                    soft_reset();

                return 0;
            }

            if ( 0x08 == ch || 0x7f == ch ) /* it's an apple 1 thing */
                ch = 0x5f;

            kbd_char = ch;
            kbd_available = true;
            return 0x80;
        }
        else
            return 0;
    }
    else if ( 0xd010 == address )
    {
        if ( kbd_available )
        {
            kbd_available = false;
            ch = kbd_char;
        }
        else
            ch = (char) bdos_kbhit();

        if ( 0 != ch )
        {
            ch = (char) toupper( ch );
            ch |= 0x80;
            set_byte( 0xd011, 0 );
            return ch;
        }
    }

    return get_byte( address );
}

void m_store( address ) uint16_t address;
{
    char ch;

    if ( 0xd012 == address )
    {
        ch = 0x7f & get_byte( 0xd012 );
        if ( 0x7f != ch && 0x0a != ch )
        {
            if ( 0x0d == ch )
                ch = 0x0a;

            printf( "%c", ch );
            fflush( stdout );
        }
        set_byte( address, ch );
    }
}

static bool ishex( c ) char c;
{
    return ( ( c >= 'A' && c <= 'F' ) || ( c >= 'a' && c <= 'f' ) || ( c >= ' ' && c <= '9' ) );
}

uint16_t hextoui( p ) char * p;
{
    char c;
    uint16_t i, result;
    result = 0;

    while ( ' ' == *p )
        p++;

    while ( c = *p )
    {
        if ( c >= '0' && c <= '9' )
            i = c - '0';
        else if ( c >= 'a' && c <= 'f' )
            i = 10 + c - 'a';
        else if ( c >= 'A' && c <= 'F' )
            i = 10 + c - 'A';
        else
            break;

        result = result * 16 + i;
        p++;
    }

    return result;
}

static uint8_t read_byte( p ) char * p;
{
    uint8_t result;
    char save;
    save = p[ 2 ];
    p[ 2 ] = 0;

    result = (uint8_t) hextoui( p );
    p[ 2 ] = save;
    return result;
}

static uint16_t read_word( p ) char * p;
{
    uint16_t result;
    char save;
    save = p[ 4 ];
    p[ 4 ] = 0;

    result = hextoui( p );
    p[ 4 ] = save;
    return result;
}

static bool load_intel( fp ) FILE * fp;
{
    char * buf;
    uint8_t reclen, rectyp, x, val;
    uint16_t offset;
    char acLine[ 120 ];

    for ( ;; )
    {
        buf = fgets( acLine, _countof( acLine), fp );

        if ( buf && strlen( buf ) >= 11 )
        {
            if ( ':' != buf[ 0 ] )
                usage( "error: input Intel HEX file is malformed" );

            reclen = read_byte( buf + 1 );
            offset = read_word( buf + 3 );
            rectyp = read_byte( buf + 7 );

            if ( 1 == rectyp ) 
                break;

            if ( 0 != rectyp  )
                usage( "file format not recognized" );

            for ( x = 0; x < reclen; x++ )
            {
                if ( feof( fp ) )
                    usage( "malformed input file" );

                val = read_byte( buf + ( 2 * x ) + 9 );
                set_byte( offset + x, val );
            }
        }
        else
            break;
    }

    fclose( fp );
    return true;
}

